package com.StringMethods;

public class CharAtMethod {

	public static void main(String[] args) {

		// CharAt methods is used to returns charactor located at the specified index
		String s = " Pranali";
		int index = 5;

		char c = s.charAt(index);

		System.out.println("The charactor at the index " + index + " is: " + c);

	}

}
